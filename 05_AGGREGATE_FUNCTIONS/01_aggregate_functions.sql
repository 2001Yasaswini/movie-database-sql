FROM CINEMA;

-- MAX Function
-- Highest rating

SELECT MAX(RATING)
FROM CINEMA;

-- MIN Function
-- Lowest rating

SELECT MIN(RATING)
FROM CINEMA;

-- AVG Function
-- Average rating

SELECT AVG(RATING)
FROM CINEMA;

-- SUM Function
-- Total collection

SELECT SUM(COLLECTION)
FROM CINEMA;
